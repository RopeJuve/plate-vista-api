import { Schema, model } from "mongoose";

const employeeSchema = new Schema(
  {
    employee: {
      type: String,
      required: true,
      unique: true
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    position: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Employee = model("Employee", employeeSchema);

export default Employee;
